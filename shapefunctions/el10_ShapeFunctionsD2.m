function [Nrr,Nss,Nrs]= el10_ShapeFunctionsD2(r,s)
%% Compute second derivatives of shape functions for 9 node quad

Nrr =[ (s*(s - 1))/2, (s*(s - 1))/2, (s*(s + 1))/2,...
       (s*(s + 1))/2, -s*(s - 1),    1 - s^2,...
       -s*(s + 1),    1 - s^2,       2*s^2 - 2];
 

 
Nss =[ 2*r*(r/4 - 1/4), (r*(r + 1))/2, (r*(r + 1))/2,...
       2*r*(r/4 - 1/4), 1 - r^2, -r*(r + 1),...
          1 - r^2, -r*(r - 1), 2*r^2 - 2];

  
 
Nrs =[ s*(r/2 - 1/4) + (r/2 - 1/4)*(s - 1),...
    s*(r/2 + 1/4) + (r/2 + 1/4)*(s - 1),...
    s*(r/2 + 1/4) + (r/2 + 1/4)*(s + 1),...
    s*(r/2 - 1/4) + (r/2 - 1/4)*(s + 1),...
    - r*s - r*(s - 1), -2*s*(r + 1/2),...
    - r*s - r*(s + 1), -2*s*(r - 1/2), 4*r*s];
